package wakitahokuto_kenshu;

public class Q0005 {

	public static void main(String[] args) {
		
		//整数型の変数iを初期値1として宣言し、{}内の処理を一度実行するごとにiを+1していき、i<=100を満たす限り繰り返し実行する
		for(int i=1;i<=100;i++) {
			
			//"Hello World"の文字列と"空白"と変数"i"と"回目"という文字列を連結して1つの文字列として出力する
			System.out.println("Hello World"+" "+i+"回目");
			
			
		}
	}

}
